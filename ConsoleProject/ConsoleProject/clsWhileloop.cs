using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleProject
{
    class clsWhileloop
    {
        static void Main(string[] args)
        {
            int i = 11;
            while (i < 10)
            {
                Console.Write(i);
                i++;
            }
            Console.ReadLine();
        }
    }
